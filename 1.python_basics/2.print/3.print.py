# 일반적인 출력
print("Hello")
print("World")

# 같은 줄에 출력하기 (end 매개변수 사용)
print("Hello", end=" ")
print("World")

# 구분자를 바꿔서 출력하기 (sep 매개변수 사용)
print("사과", "바나나", "오렌지", sep=", ")
print("2025", "06", "28", sep="-")

