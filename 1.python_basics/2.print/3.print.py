# 이것은 한 줄 주석입니다.
# 주석은 프로그램 실행에 영향을 주지 않습니다.

print("이 줄은 실행됩니다")  # 이것도 주석입니다

# 여러 줄 주석 작성법 1: 각 줄마다 # 사용
# 이것은 여러 줄 주석의
